/** Generate extra unique rows so total stays in 40–80 range without duplicate titles. */
function generatedRows(): SeedTemplate[] {
  const centers: Array<{ area: string; lat: number; lng: number }> = [
    { area: "Hayward", lat: 37.6688, lng: -122.081 },
    { area: "San Leandro", lat: 37.7249, lng: -122.1561 },
    { area: "Union City", lat: 37.5958, lng: -122.0191 },
    { area: "Castro Valley", lat: 37.6941, lng: -122.0863 },
    { area: "Fremont", lat: 37.5485, lng: -121.9886 },
    { area: "Oakland", lat: 37.8044, lng: -122.2711 },
  ];
  const categories: SeedTemplate["category"][] = [
    "food_bank",
    "community_kitchen",
    "bakery",
    "restaurant",
    "fast_food",
    "grocery",
    "supermarket",
    "cafe",
  ];
  const out: SeedTemplate[] = [];
  let n = 0;
  for (const c of centers) {
    for (let i = 0; i < 4; i++) {
      const cat = categories[n % categories.length]!;
      n++;
      const jitterLat = c.lat + (i * 0.0041 - 0.006) * (n % 2 === 0 ? 1 : -1);
      const jitterLng = c.lng + (i * 0.0053 - 0.007) * (n % 3 === 0 ? 1 : -1);
      const free = cat === "food_bank" || cat === "community_kitchen";
      const soon = n % 5 === 0;
      const status: SeedTemplate["status"] =
        n % 17 === 0 ? "reserved" : n % 23 === 0 ? "expired" : "available";
      const qty = 5 + (n % 40);
      const orig = free ? null : 10 + (n % 25);
      const disc = free ? 0 : orig != null ? Math.max(0, orig * 0.35 - (n % 4)) : null;
      out.push({
        title: `${c.area} — surplus listing #${n} (${cat.replace(/_/g, " ")})`,
        category: cat,
        resource_type: cat === "bakery" || cat === "cafe" ? "bakery" : "meal",
        quantity: status === "expired" ? 0 : qty,
        original_price: orig,
        discounted_price: disc,
        lat: Number(jitterLat.toFixed(5)),
        lng: Number(jitterLng.toFixed(5)),
        status,
        expiresHoursOffset: status === "expired" ? -6 - (n % 24) : soon ? 2 + (n % 5) : 24 + (n % 96),
      });
    }
  }
  return out;
}

export function getHaywardResourceSeedRows(): CreateResourceInput[] {
  const all = [...TEMPLATES, ...generatedRows()];
  return all.map((t) => ({
    title: t.title,
    category: t.category,
    resource_type: t.resource_type,
    quantity: t.quantity,
    original_price: t.original_price,
    discounted_price: t.discounted_price,
    lat: t.lat,
    lng: t.lng,
    status: t.status,
    expires_at: isoHoursFromNow(t.expiresHoursOffset),
  }));
}
